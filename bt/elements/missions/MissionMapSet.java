package bt.elements.missions;

public class MissionMapSet
{
	private int _Maps;
	private int _Rows;
	private int _Columns;
	
	public int getMaps()
	{
		return _Maps;
	}
	public void setMaps(int maps)
	{
		_Maps = maps;
	}
	public int getRows()
	{
		return _Rows;
	}
	public void setRows(int rows)
	{
		_Rows = rows;
	}
	public int getColumns()
	{
		return _Columns;
	}
	public void setColumns(int columns)
	{
		_Columns = columns;
	}
	
	
}
