package as.elements;

public class FactionEraUnitLink
{
	private int _Faction;
	private int _Era;
	private int _Unit;
	
	public int getFaction()
	{
		return _Faction;
	}
	public void setFaction(int faction)
	{
		_Faction = faction;
	}
	public int getEra()
	{
		return _Era;
	}
	public void setEra(int era)
	{
		_Era = era;
	}
	public int getUnit()
	{
		return _Unit;
	}
	public void setUnit(int unit)
	{
		_Unit = unit;
	}
	
	
}
